# Definición de Árboles


class ArbolBinario
  attr_accessor :value
  attr_accessor :left
  attr_accessor :right
  
  def initialize val, left, right
      @value = val
      @left = left
      @right = right
  end
end

	
class ArbolRosa
  attr_accessor :value
  
  def initialize value
      @value = val
  end
end
